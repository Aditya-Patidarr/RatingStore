const { DataTypes } = require('sequelize');
const sequelize = require('../config/db.config');

const Rating = sequelize.define('Rating', {
    userId: {
        type: DataTypes.INTEGER,
        allowNull: false,
        references: {
            model: 'Users',
            key: 'id'
        }
    },
    storeId: {
        type: DataTypes.INTEGER,
        allowNull: false,
        references: {
            model: 'Stores',
            key: 'id'
        }
    },
    rating: {
        type: DataTypes.INTEGER,
        allowNull: false,
        validate: {
            min: 1,
            max: 5
        }
    },
    comment: {
        type: DataTypes.STRING(400),
        allowNull: true
    }
}, {
    timestamps: true
});


module.exports = Rating;
